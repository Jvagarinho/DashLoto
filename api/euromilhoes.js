const axios = require('axios');
const cheerio = require('cheerio');

async function handler(req, res) {
    const BASE_URL = 'https://www.jogossantacasa.pt/web/SCCartazResult';
    
    res.setHeader('Access-Control-Allow-Origin', '*');
    res.setHeader('Access-Control-Allow-Methods', 'GET, OPTIONS');
    res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
    
    if (req.method === 'OPTIONS') {
        return res.status(200).end();
    }
    
    try {
        console.log('Scraping Euromilhões...');
        
        const response = await axios.get(BASE_URL + '/', {
            headers: {
                'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36',
                'Accept': 'text/html,application/xhtml+xml',
                'Accept-Language': 'pt-PT,pt;q=0.9'
            },
            timeout: 20000
        });

        const $ = cheerio.load(response.data);
        
        let numbers = [];
        let stars = [];
        let date = '12/05/2026';
        
        const target = $('.betMiddle.twocol.regPad ul.colums li');
        
        if (target.length > 0) {
            target.each((i, el) => {
                if (i >= 7) return;
                
                const text = $(el).text().trim();
                const num = parseInt(text);
                
                if (!isNaN(num) && num >= 1 && num <= 50 && !numbers.includes(num) && !stars.includes(num)) {
                    if (numbers.length < 5 && num <= 50) {
                        numbers.push(num);
                    } else if (stars.length < 2 && num <= 12) {
                        stars.push(num);
                    }
                }
            });
        }
        
        console.log('Euromilhões extraídos:', { numbers, stars });
        
        if (numbers.length >= 5 && stars.length >= 2) {
            numbers.sort((a, b) => a - b);
            stars.sort((a, b) => a - b);
            res.status(200).json({ numbers, stars, date });
        } else {
            res.status(200).json({ 
                numbers: [4, 26, 32, 35, 36], 
                stars: [5, 7], 
                date: '12/05/2026',
                source: 'fallback'
            });
        }
    } catch (error) {
        console.error('Erro:', error.message);
        res.status(200).json({ 
            numbers: [4, 26, 32, 35, 36], 
            stars: [5, 7], 
            date: '12/05/2026',
            source: 'fallback'
        });
    }
}

module.exports = handler;